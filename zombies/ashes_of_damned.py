def search_sections(query: str):
    """–ü–æ–∏—Å–∫ –ø–æ title/text/keywords. –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ø–∏—Å–æ–∫ —Å–µ–∫—Ü–∏–π (–≤ –ø–æ—Ä—è–¥–∫–µ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π)."""
    q = (query or "").strip().lower()
    if not q:
        return []
    hits = []
    for s in SECTIONS:
        blob = (s["title"] + " " + s["text"] + " " + " ".join(s.get("keywords") or [])).lower()
        if q in blob:
            hits.append(s)
    return hits
