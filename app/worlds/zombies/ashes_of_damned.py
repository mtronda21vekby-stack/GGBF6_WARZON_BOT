def list_buttons():
    return [(s["id"], s["title"]) for s in SECTIONS]


def get_section(section_id: str):
    for s in SECTIONS:
        if s["id"] == section_id:
            return s
    return None


def search_sections(query: str):
    q = (query or "").strip().lower()
    if not q:
        return []
    hits = []
    for s in SECTIONS:
        blob = (s["title"] + " " + s["text"] + " " + " ".join(s.get("keywords") or [])).lower()
        if q in blob:
            hits.append(s)
    return hits
