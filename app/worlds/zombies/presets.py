def _p(prof: Dict[str, Any], key: str, default: str = "") -> str:
    v = (prof or {}).get(key)
    return str(v).strip() if v is not None and str(v).strip() else default


def _fmt(title: str, lines: List[str]) -> str:
    out = [title, ""]
    out.extend(lines)
    return "\n".join(out).strip()


def _norm_map(profile: Dict[str, Any]) -> str:
    """
    –ö–∞—Ä—Ç–∞ –∑–æ–º–±–∏ –±–µ—Ä—ë—Ç—Å—è –∏–∑ profile["zombies_map"].
    –ï—Å–ª–∏ –Ω–µ—Ç ‚Äî –¥–µ—Ñ–æ–ª—Ç Ashes.
    """
    m = _p(profile or {}, "zombies_map", "Ashes").strip().lower()
    if "astra" in m:
        return "Astra"
    return "Ashes"


